    private void txtloginKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtloginKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        int limite = 13;
        if (Character.isDigit(c)) {
            evt.consume();
        }
        if (txtlogin.getText().length() == limite) {
            evt.consume();
        }
    }//GEN-LAST:event_txtloginKeyTyped

    private void btnbuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnbuscarKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        int limiete = 9;
        if (!Character.isDigit(c)) {//si es diferente a letra...
            evt.consume();
        }
        if (btnbuscar.getText().length() == limiete) {
            evt.consume();
        }
    }//GEN-LAST:event_btnbuscarKeyTyped

